package com.yoitai.glib;

//============================================================================
//                           カラー管理
//============================================================================
public class Color {
	// ===================================================
	//                      置換文字
	// ===================================================

	// ===================================================
	//                    メンバー定義
	// ===================================================
	public float R;
	public float G;
	public float B;
	public float A;

	// ===================================================
	//                     メソッド
	// ===================================================
	// ---------------------------------------------------
	//                   コンストラクタ
	// ---------------------------------------------------
	public Color(float _r ,float _g ,float _b ,float _a)
	{
		R = _r;
		G = _g;
		B = _b;
		A = _a;
	}

	// ---------------------------------------------------
	//                   コンストラクタ
	// ---------------------------------------------------
	public Color()
	{
	}

	// ---------------------------------------------------
	//                  セット
	// ---------------------------------------------------
	public void Set(float _r ,float _g ,float _b ,float _a)
	{
		R = _r;
		G = _g;
		B = _b;
		A = _a;
	}

	// ---------------------------------------------------
	//                   コピー
	// ---------------------------------------------------
	public void Copy(Color _src)
	{
		R = _src.R;
		G = _src.G;
		B = _src.B;
		A = _src.A;
	}
}

