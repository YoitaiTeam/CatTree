package com.yoitai.glib;

//============================================================================
//                       ２次元座標クラス
//============================================================================
public class Vector2 {
	// ===================================================
	//                      置換文字
	// ===================================================

	// ===================================================
	//                    メンバー定義
	// ===================================================
	public float X;
	public float Y;

	// ===================================================
	//                     メソッド
	// ===================================================
	// ---------------------------------------------------
	//                   コンストラクタ
	// ---------------------------------------------------
	public Vector2(float _x ,float _y)
	{
		X = _x;
		Y = _y;
	}

	// ---------------------------------------------------
	//                   コンストラクタ
	// ---------------------------------------------------
	public Vector2()
	{
		X = 0;
		Y = 0;
	}

	// ---------------------------------------------------
	//                   コピー
	// ---------------------------------------------------
	public void Copy(Vector2 _src)
	{
		X = _src.X;
		Y = _src.Y;
	}

	// ---------------------------------------------------
	//                   設定
	// ---------------------------------------------------
	public void Set(float _x ,float _y)
	{
		X = _x;
		Y = _y;
	}

	// ---------------------------------------------------
	//                       内積
	// ---------------------------------------------------
	public float Dot(Vector2 _src)
	{
		return(X*_src.X + Y*_src.Y);
	}

	//=========================================================
	// 長さ
	//=========================================================
	public float CalcLength()
	{
		return((float)Math.sqrt(X*X+Y*Y));
	}

	//=========================================================
	// 正規化
	//=========================================================
	public boolean Normalize()
	{
		float l;

		l = CalcLength();
		if( l == 0.0f )return(false);
		if( l == 1.0f )return(true);

		X /= l;
		Y /= l;

		return(true);
	}

	// ---------------------------------------------------
	//                   加算
	// ---------------------------------------------------
	public void Add(Vector2 _src)
	{
		Add(_src ,null);
	}
	public void Add(Vector2 _src ,Vector2 _dst)
	{
		if( _dst == null ){
			X += _src.X;
			Y += _src.Y;
		}else{
			_dst.X = X + _src.X;
			_dst.Y = Y + _src.Y;
		}
	}

	// ---------------------------------------------------
	//                   減算
	// ---------------------------------------------------
	public void Subtract(Vector2 _src)
	{
		Subtract(_src ,null);
	}
	public void Subtract(Vector2 _src ,Vector2 _dst)
	{
		if( _dst == null ){
			X -= _src.X;
			Y -= _src.Y;
		}else{
			_dst.X = X - _src.X;
			_dst.Y = Y - _src.Y;
		}
	}

	// ---------------------------------------------------
	//                   乗算
	// ---------------------------------------------------
	public void Multiply(float _t)
	{
		Multiply(_t ,null);
	}
	public void Multiply(float _t ,Vector2 _dst)
	{
		if( _dst == null ){
			X *= _t;
			Y *= _t;
		}else{
			_dst.X = X * _t;
			_dst.Y = Y * _t;
		}
	}

	// ---------------------------------------------------
	//                   除算
	// ---------------------------------------------------
	public void Divide(float _t)
	{
		Divide(_t ,null);
	}
	public void Divide(float _t ,Vector2 _dst)
	{
		if( _dst == null ){
			X /= _t;
			Y /= _t;
		}else{
			_dst.X = X / _t;
			_dst.Y = Y / _t;
		}
	}
}
